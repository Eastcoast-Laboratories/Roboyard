                    val newLeafArray = tryAllocateLeafArray()
                    if (newLeafArray == null) return false
                    this.leafArrays[this.numLeafArrays++] = newLeafArray
                    this.nextLeafArray += LEAF_ARRAY_SIZE
                }
                leafIndex = this.nextLeaf
                this.nextLeaf += this.leafSize
                nodeArray[nidx] = leafIndex
                //push the previous "compressed branch" further to the leaf
                val lidx = (leafIndex and LEAF_ARRAY_MASK) + (prevKey and this.leafMask)
                this.leafArrays[leafIndex ushr LEAF_ARRAY_SHIFT][lidx] = prevVal.toByte()
            }
            val leafArray = this.leafArrays[leafIndex ushr LEAF_ARRAY_SHIFT]
            val lidx = (leafIndex and LEAF_ARRAY_MASK) + (key.toInt() and this.leafMask)
            val prevVal = leafArray[lidx]
            if (byteValue > (0xff and prevVal.toInt())) {  //putIfGreater
                leafArray[lidx] = byteValue.toByte()
                return true
            }
            return false
        }

        override fun allocatedBytes(): Long {
            return super.allocatedBytes() + this.lookupArray.size * 4L
        }

        companion object {
            const val LOOKUP_SHIFT: Int = 3 * 8
            const val LOOKUP_SHIFT_2: Int = 2 * 8
            const val LOOKUP_MASK: Int = (1 shl LOOKUP_SHIFT) - 1
        }
    }


    /**
     * this class is used to find a larger number of distinct, equally good solutions,
     * especially those that share some robot positions and would normally be excluded
     * by the aggressive search tree pruning.
     * the methods "putIfGreater" are modified to behave like "putIfGreaterOrEqual".
     * (the code is copied from the super class, changed lines are marked with //putIfGreaterOrEqual)
     */
    private class KeyDepthMapTrieSpecial8BitEqual(board: Board) :
        KeyDepthMapTrieSpecial8Bit(board) {
        override fun putIfGreater(key: Int, byteValue: Int): Boolean { // for 4 robots
            var key = key
            var nidx = key and LOOKUP_MASK
            var nodeIndex = this.lookupArray[nidx]
            val nodeArray: IntArray
            key = key ushr LOOKUP_SHIFT
            if (0 == nodeIndex) {
                // -> node index is null = unused
                //write current key+value as a "compressed branch" (negative node index)
                //exit immediately because no further nodes and no leaf need to be stored
                this.lookupArray[nidx] = ((key.inv()) shl 8) or byteValue //negative
                return true
            } else if (0 > nodeIndex) {
                // -> node index is negative = used by a single "compressed branch"
                val prevKey = (nodeIndex.inv()) shr 8
                val prevVal = 0xff and nodeIndex
                //previous and current keys are equal (duplicate key)
                if (prevKey == key) {
                    if (byteValue >= prevVal) {  //putIfGreaterOrEqual
                        this.lookupArray[nidx] = (nodeIndex xor prevVal) or byteValue //negative
                        return true
                    }
                    return false
                }
                //previous and current keys are not equal
                //create a new node
                if (this.nextNode + this.leafNodeSize > this.nextNodeArray) {
                    if (this.nodeArrays.size <= this.numNodeArrays) {
                        this.nodeArrays =
                            this.nodeArrays.copyOf<IntArray>(this.nodeArrays.size shl 1) as Array<IntArray>
                    }
                    val newNodeArray = tryAllocateNodeArray()
                    if (newNodeArray == null) return false
                    this.nodeArrays[this.numNodeArrays++] = newNodeArray
                    this.nextNode = this.nextNodeArray
                    this.nextNodeArray += NODE_ARRAY_SIZE
                }
                nodeIndex = this.nextNode
                this.nextNode += this.leafNodeSize
                this.lookupArray[nidx] = nodeIndex
                //push previous "compressed branch" one node further
                nodeArray = this.nodeArrays[nodeIndex ushr NODE_ARRAY_SHIFT]
                nidx = (nodeIndex and NODE_ARRAY_MASK) + (prevKey and this.leafNodeMask)
                nodeArray[nidx] =
                    ((prevKey ushr this.leafNodeShift).inv() shl 8) or prevVal //negative
            } else {
                // -> node index is positive = go to next node
                nodeArray = this.nodeArrays[nodeIndex ushr NODE_ARRAY_SHIFT]
            }
            nidx = (nodeIndex and NODE_ARRAY_MASK) + (key and this.leafNodeMask)
            //get leaf (with compression)
            var leafIndex = nodeArray[nidx]
            key = key ushr this.leafNodeShift
            if (0 == leafIndex) {
                // -> leaf index is null = unused
                //write current value as a "compressed branch" (negative leaf index)
                //exit immediately because no leaf needs to be stored
                nodeArray[nidx] = ((key.inv()) shl 8) or byteValue //negative
                return true
            } else if (0 > leafIndex) {
                // -> leaf index is negative = used by a single "compressed branch"
                val prevKey = (leafIndex.inv()) shr 8
                val prevVal = 0xff and leafIndex
                //previous and current keys are equal (duplicate key)
                if (prevKey == key) {
                    if (byteValue >= prevVal) {  //putIfGreaterOrEqual
                        nodeArray[nidx] = (leafIndex xor prevVal) or byteValue //negative
                        return true
                    }
                    return false
                }
                //previous and current keys are not equal
                //create a new leaf
                if (this.nextLeaf >= this.nextLeafArray) {
                    if (this.leafArrays.size <= this.numLeafArrays) {
                        this.leafArrays =
                            this.leafArrays.copyOf<ByteArray>(this.leafArrays.size shl 1) as Array<ByteArray>
                    }
                    val newLeafArray = tryAllocateLeafArray()
                    if (newLeafArray == null) return false
                    this.leafArrays[this.numLeafArrays++] = newLeafArray
                    this.nextLeafArray += LEAF_ARRAY_SIZE
                }
                leafIndex = this.nextLeaf
                this.nextLeaf += this.leafSize
                nodeArray[nidx] = leafIndex
                //push the previous "compressed branch" further to the leaf
                val lidx = (leafIndex and LEAF_ARRAY_MASK) + (prevKey and this.leafMask)
                this.leafArrays[leafIndex ushr LEAF_ARRAY_SHIFT][lidx] = prevVal.toByte()
            }
            val leafArray = this.leafArrays[leafIndex ushr LEAF_ARRAY_SHIFT]
            val lidx = (leafIndex and LEAF_ARRAY_MASK) + (key and this.leafMask)
            val prevVal = leafArray[lidx]
            if (byteValue >= (0xff and prevVal.toInt())) {  //putIfGreaterOrEqual
                leafArray[lidx] = byteValue.toByte()
                return true
            }
            return false
        }

        override fun putIfGreater(key: Long, byteValue: Int): Boolean { // for 5 robots
            var key = key
            var nidx = key.toInt() and LOOKUP_MASK
            var nodeIndex = this.lookupArray[nidx]
            var nodeArray: IntArray
            key = key ushr LOOKUP_SHIFT
            var elementThis = (nidx ushr LOOKUP_SHIFT_2)
            var elementThisLookup = this.elementLookup[elementThis]
            //go through nodes (with compression because (key<<8)+value is inside "int" range now)
            if (0 == nodeIndex) {
                // -> node index is null = unused
                //write current key+value as a "compressed branch" (negative node index)
                //exit immediately because no further nodes and no leaf need to be stored
                this.lookupArray[nidx] = ((key.toInt().inv()) shl 8) or byteValue //negative
                return true
            } else if (0 > nodeIndex) {
                // -> node index is negative = used by a single "compressed branch"
                val prevKey = (nodeIndex.inv()) shr 8
                val prevVal = 0xff and nodeIndex
                //previous and current keys are equal (duplicate key)
                if (prevKey == key.toInt()) {
                    if (byteValue >= prevVal) {  //putIfGreaterOrEqual
                        this.lookupArray[nidx] = (nodeIndex xor prevVal) or byteValue //negative
                        return true
                    }
                    return false
                }
                //previous and current keys are not equal
                //create a new node
                val nodeSize = this.nodeSizeLookup[elementThis]
                if (this.nextNode + nodeSize > this.nextNodeArray) {
                    if (this.nodeArrays.size <= this.numNodeArrays) {
                        this.nodeArrays =
                            this.nodeArrays.copyOf<IntArray>(this.nodeArrays.size shl 1) as Array<IntArray>
                    }
                    val newNodeArray = tryAllocateNodeArray()
                    if (newNodeArray == null) return false
                    this.nodeArrays[this.numNodeArrays++] = newNodeArray
                    this.nextNode = this.nextNodeArray
                    this.nextNodeArray += NODE_ARRAY_SIZE
                }
                nodeIndex = this.nextNode
                this.nextNode += nodeSize
                this.lookupArray[nidx] = nodeIndex
                //push previous "compressed branch" one node further
                nodeArray = this.nodeArrays[nodeIndex ushr NODE_ARRAY_SHIFT]
                elementThis = key.toInt() and this.nodeMask
                nidx = (nodeIndex and NODE_ARRAY_MASK) - elementThisLookup - 1
                elementThisLookup = this.elementLookup[elementThis]
                nodeArray[nidx + this.elementLookup[prevKey and this.nodeMask]] =
                    ((prevKey ushr this.nodeShift).inv() shl 8) or prevVal
            } else {
                // -> node index is positive = go to next node
                elementThis = key.toInt() and this.nodeMask
                nidx = (nodeIndex and NODE_ARRAY_MASK) - elementThisLookup - 1
                elementThisLookup = this.elementLookup[elementThis]
                nodeArray = this.nodeArrays[nodeIndex ushr NODE_ARRAY_SHIFT]
            }
            nidx += elementThisLookup
            //go through leaf node (with compression)
            nodeIndex = nodeArray[nidx]
            key = key ushr this.nodeShift
            if (0 == nodeIndex) {
                // -> node index is null = unused
                //write current key+value as a "compressed branch" (negative node index)
                //exit immediately because no further nodes and no leaf need to be stored
                nodeArray[nidx] = ((key.toInt().inv()) shl 8) or byteValue //negative
                return true
            } else if (0 > nodeIndex) {
                // -> node index is negative = used by a single "compressed branch"
                val prevKey = (nodeIndex.inv()) shr 8
                val prevVal = 0xff and nodeIndex
                //previous and current keys are equal (duplicate key)
                if (prevKey == key.toInt()) {
                    if (byteValue >= prevVal) {  //putIfGreaterOrEqual
                        nodeArray[nidx] = (nodeIndex xor prevVal) or byteValue //negative
                        return true
                    }
                    return false
                }
                //previous and current keys are not equal
                //create a new node
                if (this.nextNode + this.leafNodeSize > this.nextNodeArray) {
                    if (this.nodeArrays.size <= this.numNodeArrays) {
                        this.nodeArrays =
                            this.nodeArrays.copyOf<IntArray>(this.nodeArrays.size shl 1) as Array<IntArray>
                    }
                    val newNodeArray = tryAllocateNodeArray()
                    if (newNodeArray == null) return false
                    this.nodeArrays[this.numNodeArrays++] = newNodeArray
                    this.nextNode = this.nextNodeArray
                    this.nextNodeArray += NODE_ARRAY_SIZE
                }
                nodeIndex = this.nextNode
                this.nextNode += this.leafNodeSize
                nodeArray[nidx] = nodeIndex
                //push previous "compressed branch" one node further
                nodeArray = this.nodeArrays[nodeIndex ushr NODE_ARRAY_SHIFT]
                nidx = (nodeIndex and NODE_ARRAY_MASK) + (prevKey and this.leafNodeMask)
                nodeArray[nidx] =
                    ((prevKey ushr this.leafNodeShift).inv() shl 8) or prevVal //negative
            } else {
                // -> node index is positive = go to next node
                nodeArray = this.nodeArrays[nodeIndex ushr NODE_ARRAY_SHIFT]
            }
            nidx = (nodeIndex and NODE_ARRAY_MASK) + (key.toInt() and this.leafNodeMask)
            //get leaf (with compression)
            var leafIndex = nodeArray[nidx]
            key = key ushr this.leafNodeShift
            if (0 == leafIndex) {
                // -> leaf index is null = unused
                //write current value as a "compressed branch" (negative leaf index)
                //exit immediately because no leaf needs to be stored
                nodeArray[nidx] = ((key.toInt().inv()) shl 8) or byteValue //negative
                return true
            } else if (0 > leafIndex) {
