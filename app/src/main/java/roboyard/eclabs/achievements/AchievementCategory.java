package roboyard.eclabs.achievements;

/**
 * Categories for achievements.
 */
public enum AchievementCategory {
    PROGRESSION,      // Level completion achievements
    PERFORMANCE,      // Optimal moves, speedruns
    CHALLENGE,        // No hints, custom levels
    MASTERY,          // 3-star achievements
    SPECIAL,          // First game, streaks, comeback
    RANDOM_DIFFICULTY,    // Impossible mode
    RANDOM_SOLUTION,      // Solution length achievements
    RANDOM_RESOLUTION,    // Screen resolution achievements
    RANDOM_TARGETS,       // Multiple targets
    RANDOM_ROBOTS,        // Robot count
    RANDOM_COVERAGE,      // Square coverage
    RANDOM_STREAKS,       // Random game streaks
    RANDOM_SPEED          // Speed achievements
}
